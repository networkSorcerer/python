# 표준 입출력 : 콘솔 입출을 의미"
# [] 대괄호 : 리스트를 표시
# {} 중괄호 : 딕셔너리 표시
# () 소괄호 : 함수의 인수, 튜플 사용
# print() : 화면 출력을 위한 함수
print(36)
print("문자열")
print([1,2,3]) #리스트 출력
print("파" + "이" + "썬") # + 문자열을 이어주는 연산자
print("파","이","썬", sep="") # , (콤마) 구분자를 의미, 구분자의 기본값을 의미
print("파""이""썬")
# 이스케이프 문자 : 출력 구간의 흐름을 제어
# \n(줄바꿈), \t (탭을 의미), \b(백스페이스)를 의미. \r(커서를 맨 앞으로 돌림)

